using System;
using System.Collections.Generic;

namespace Leetspeakname
{
  public class Leet
  {
    public string PlayGame(index)
    {
      if (index == "e")
      {
        return "3";
      }
      else if (index == "o")
      {
        return "0";
      }
      else if (index == "I")
      {
        return "1";
      }
      else if (index == "t")
      {
        return "7";
      }
      else if ( index == "s")
      {
        return "z";
      }
      else
      {
        return "15";
      }
    }
  }
}
